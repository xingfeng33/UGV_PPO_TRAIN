# =============================================================================
# 模块 1: 超参数管理 (Hyperparameters Config) —— v3 探索修复版
#
# 修改记录 (v2 → v3):
#   [Fix-1] entropy_coef: 0.005 → 0.02
#           原因：v2 的 0.005 是导致 Entropy 暴跌的直接原因。
#                 对于连续高斯策略，0.01~0.05 是学界标准范围。
#                 0.02 在"促进探索"和"不妨碍收敛"之间取得平衡。
#
#   [Fix-2] lr: 3e-4 → 1e-4
#           原因：学习率过高是策略过快坍塌的帮凶。
#                 降低学习率，让 log_std 的下降速度变慢，给探索更多时间。
#
#   [Fix-3] ppo_epochs: 10 → 6
#           原因：每轮数据被重复利用 10 次，会使策略在少量数据上过度拟合，
#                 加速 Entropy 坍塌。6 次是更保守稳健的选择。
#
#   [Fix-4] clip_ratio: 0.2 → 0.2（保持不变，此值本身合理）
#
#   [Fix-5] rollout_steps: 4096 → 8192
#           原因：增大每次策略更新前收集的数据量，
#                 一个 rollout 中能看到更多样的状态，
#                 优势估计更稳定，策略梯度方差更小，
#                 不容易被单一局部最优"锁定"。
#
#   [Fix-6] mini_batch_size: 256 → 512（配合 rollout_steps 扩大）
#
#   [Fix-7] total_timesteps: 2_000_000 → 5_000_000
#           原因：���疏奖励任务通常需要更多步数，
#                 200 万步对于 1610 维输入的搜索任务可能严重不足。
#
# 奖励函数说明（需要同步修改 Unity 端 UGVAgent.cs）：
#   当前 C# 端奖励设计存在稀疏性问题，单靠算法侧调参难以根本解决。
#   建议在 UGVAgent.cs 中增加"靠近目标的 shaping reward"。
#   详见下方注释。
# =============================================================================

from dataclasses import dataclass, field
from typing import List


@dataclass
class PPOConfig:

    # ------------------------------------------------------------------
    # [环境配置]
    # ------------------------------------------------------------------
    env_path: str = None
    worker_id: int = 0

    # Unity 环境中 Behavior 的名称
    behavior_name: str = "UGV"

    # 状态维度（禁用 Camera 后的纯射线模式）
    # 运行时会自动检测覆盖，此值作为备用
    state_dim: int = 1610

    # 动作空间维度: [Steer, Motor]
    action_dim: int = 2

    # ------------------------------------------------------------------
    # [PPO 核心超参数]
    # ------------------------------------------------------------------

    # [Fix-2] 学习率降低：3e-4 → 1e-4
    # 原因：过高的学习率使 log_std 快速坍塌，降低给探索更多时间
    lr: float = 1e-4

    # 折扣因子 γ：保持 0.99，长远目标任务适合高折扣
    gamma: float = 0.99

    # GAE λ：保持 0.95（经典设置）
    gae_lambda: float = 0.95

    # PPO Clip 系数 ε：保持 0.2
    clip_ratio: float = 0.2

    # [Fix-1] 熵系数大幅提升：0.005 → 0.02
    # 原因：v2 的 0.005 是 Entropy 暴跌的直接元凶
    #       0.02 能有效对抗 log_std 的过快下降
    #       如果训练初期 Entropy 下降还是太快，可进一步提升至 0.03
    entropy_coef: float = 0.02

    # Critic 损失权重：保持 0.5
    value_loss_coef: float = 0.5

    # 梯度裁剪：保持 0.5
    max_grad_norm: float = 0.5

    # ------------------------------------------------------------------
    # [训练流程参数]
    # ------------------------------------------------------------------

    # [Fix-5] 每个 Rollout 步数扩大：4096 → 8192
    # 原因：更大的 rollout 能收集更多样化的状态，
    #       减少策略被单一局部最优锁定的风险，
    #       优势函数估计更稳定
    rollout_steps: int = 8192

    # [Fix-6] mini-batch 大小同步扩大：256 → 512
    mini_batch_size: int = 512

    # [Fix-3] PPO epoch 数减少：10 → 6
    # 原因：10 次复用同一批数据容易导致过拟合和 Entropy 坍塌
    #       6 次在数据效率和策略稳定性之间更平衡
    ppo_epochs: int = 6

    # [Fix-7] 总训练步数扩大：2_000_000 → 5_000_000
    # 原因：稀疏奖励搜索任务，200 万步严重不足
    #       500 万步是更合理的基线
    total_timesteps: int = 5_000_000

    # ------------------------------------------------------------------
    # [网络结构参数]（保持不变）
    # ------------------------------------------------------------------

    # 1610维输入 → 256 隐层，比例合理，无需扩大
    mlp_hidden_dims: List[int] = field(default_factory=lambda: [256, 256])

    # 特征输出维度
    feature_dim: int = 128

    # ------------------------------------------------------------------
    # [日志与保存参数]
    # ------------------------------------------------------------------
    log_dir: str = "runs/ugv_ppo"
    checkpoint_dir: str = "checkpoints/ugv_ppo"

    # [Fix-8] 保存间隔减小：50 → 25
    # 原因：步数更大后，每 25 次 rollout 保存一次，
    #       方便捕捉训练过程中短暂出现的好结果
    save_interval: int = 25

    # 评估间隔：保持 10
    eval_interval: int = 10

    # 评估 episode 数：保持 5
    eval_episodes: int = 5

    seed: int = 42
    device: str = "cuda"